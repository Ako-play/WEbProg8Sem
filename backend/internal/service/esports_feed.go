// EsportsArticle — карточка материала (патч, новость, режим). Body — развёрнутый текст для свежих патчей.
type EsportsArticle struct {
	ID      string `json:"id"`
	Title   string `json:"title"`
	Summary string `json:"summary"`
	Date    string `json:"date"`
	Body    string `json:"body,omitempty"`
}

// MatchResult — результат пары в сетке (демо-данные).
type MatchResult struct {
	Phase  string `json:"phase"`
	TeamA  string `json:"teamA"`
	TeamB  string `json:"teamB"`
	Winner string `json:"winner"`
	Score  string `json:"score"`
}

// Competition — турнир / лига.
type Competition struct {
	ID        string        `json:"id"`
	Title     string        `json:"title"`
	Region    string        `json:"region"`
	PrizePool string        `json:"prizePool"`
	Dates     string        `json:"dates"`
	Status    string        `json:"status"`
	Matches   []MatchResult `json:"matches"`
}

func mr(phase, teamA, teamB, winner, score string) MatchResult {
	return MatchResult{Phase: phase, TeamA: teamA, TeamB: teamB, Winner: winner, Score: score}
}

